    static private final String SQL_CREATE_TABLE_3 =
            "CREATE TABLE records (" +
                    "  _id INTEGER PRIMARY KEY AUTOINCREMENT," +
                    "  user INTEGER," +
                    "  caption TEXT," +
                    "  image TEXT," +
                    "  time TEXT);";// +      //generated by java
